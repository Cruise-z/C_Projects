uint32_t R1 = 0, R2 = 0;

void LFSR_init_Mode(uint32_t (*reg)[16], uint32_t u){
    uint64_t s, v;
    v = (1ull<<15)*(*reg)[15] + (1ull<<17)*(*reg)[13] + (1ull<<21)*(*reg)[10]
        + (1ull<<20)*(*reg)[4] + (1ull+(1ull<<8))*(*reg)[0];
    v %= ((1ull<<31)-1);
    s = (v+u) % ((1ull<<31)-1);
    if(s == 0)
        s = ((1ull<<31)-1);
    int cnt;
    for(cnt = 0; cnt < 15; cnt++)
        (*reg)[cnt] = (*reg)[cnt+1];
    (*reg)[15] = s;
}

void LFSR_work_Mode(uint32_t (*reg)[16]){
    uint64_t s;
    s = (1ull<<15)*(*reg)[15] + (1ull<<17)*(*reg)[13] + (1ull<<21)*(*reg)[10]
        + (1ull<<20)*(*reg)[4] + (1ull+(1ull<<8))*(*reg)[0];
    s %= ((1ull<<31)-1);
    if(s == 0)
        s = ((1ull<<31)-1);
    int cnt;
    for(cnt = 0; cnt < 15; cnt++)
        (*reg)[cnt] = (*reg)[cnt+1];
    (*reg)[15] = s;
}

void BR(uint32_t (*X)[4], const uint32_t reg[16]){
    (*X)[0] = ((reg[15]>>15)<<16) + (reg[14]&0x0000ffff);
    (*X)[1] = ((reg[11]&0x0000ffff)<<16) + (reg[ 9]>>15);
    (*X)[2] = ((reg[ 7]&0x0000ffff)<<16) + (reg[ 5]>>15);
    (*X)[3] = ((reg[ 2]&0x0000ffff)<<16) + (reg[ 0]>>15);
}

uint32_t L1(uint32_t x){
    return x ^ r_sl(x, 2) ^ r_sl(x, 10) ^ r_sl(x, 18) ^r_sl(x, 24);
}

uint32_t L2(uint32_t x){
    return x ^ r_sl(x, 8) ^ r_sl(x, 14) ^ r_sl(x, 22) ^r_sl(x, 30);
}

uint32_t ZUC_S(uint32_t x){
    uint32_t x0 = x >> 24;
    uint32_t x1 = (x & 0x00ff0000) >> 16;
    uint32_t x2 = (x & 0x0000ff00) >> 8;
    uint32_t x3 = (x & 0x000000ff);
    return (ZUC_S0_box[x0>>4][x0&0xf] << 24) + (ZUC_S1_box[x1>>4][x1&0xf] << 16)
         + (ZUC_S0_box[x2>>4][x2&0xf] <<  8) + ZUC_S1_box[x3>>4][x3&0xf];
}

void ZUC_F(uint32_t *W, uint32_t X0, uint32_t X1, uint32_t X2){
    uint32_t w1, w2;
    *W = (X0^R1) + R2;
    w1 = R1 + X1;
    w2 = R2 ^ X2;
    R1 = ZUC_S(L1(((w1&0x0000ffff)<<16)+((w2&0xffff0000)>>16)));
    R2 = ZUC_S(L2(((w2&0x0000ffff)<<16)+((w1&0xffff0000)>>16)));
}

