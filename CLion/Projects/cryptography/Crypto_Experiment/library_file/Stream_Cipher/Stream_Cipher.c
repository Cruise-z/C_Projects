void BBS_random(mpz_t *ans, int digit, mpz_t p, mpz_t q, mpz_t s){
    mpz_t result, n, temp1, x, b, two;
    mpz_init_set_ui(result, 0);
    mpz_init(n);
    mpz_init(temp1);
    mpz_init(x);
    mpz_mul(n, p, q);
    mpz_init(b);
    mpz_init_set_ui(two, 2);
    //计算种子
    mpz_pow_ui(temp1, s, 2);
    mpz_mod(x, temp1, n);
    int cnt;
    for(cnt = 0; cnt < digit; cnt++){
        mpz_pow_ui(x, x, 2);
        mpz_mod(x, x, n);
        mpz_mod_ui(b, x, 2);
        mpz_pow_ui(temp1, two, cnt);
        mpz_mul(temp1, temp1, b);
        mpz_add(result, result, temp1);
    }
    mpz_set(*ans, result);
}

uint32_t* Mersenne_Twister(uint32_t seed, int num){
    seed_Init(seed);
    twist();
    uint32_t *ans = malloc(sizeof(uint32_t)*num);
    int cnt;
    for(cnt = 0; cnt < num; cnt++)
        ans[cnt] = extract();
    return ans;
}

void seed_Init(const uint32_t seed){
    uint32_t  i;
    MT[0] = seed;
    for ( i = 1; i < N; i++ )
        MT[i] = (F * (MT[i - 1] ^ (MT[i - 1] >> 30)) + i);
    Index = N;
}

void twist(){
    uint32_t  i, x, xA;
    for ( i = 0; i < N; i++ )
    {
        x = (MT[i] & MASK_UPPER) + (MT[(i + 1) % N] & MASK_LOWER);
        xA = x >> 1;
        if ( x & 0x1 )
        {
            xA ^= A;
        }
        MT[i] = MT[(i + M) % N] ^ xA;
    }
    Index = 0;
}

uint32_t extract(){
    uint32_t  y;
    int i = Index;
    if ( Index >= N )
    {
        twist();
        i = Index;
    }
    y = MT[i];
    Index = i + 1;
    y ^= (y >> U);
    y ^= (y << S) & B;
    y ^= (y << T) & C;
    y ^= (y >> L);
    return y;
}

